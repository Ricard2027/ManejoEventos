    private void btnCalcularHorasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcularHorasMouseClicked
       int Numb1H, Numb2H,TotalH;
       
       Numb1H=Integer.parseInt(txtMesesTrabajoH.getText());
       Numb2H=Integer.parseInt(txtHorasTrabajoD.getText());
       
       //Coloqué 30 por la cantidad de días laborales que se toma, y se multiplica la cantidad de horas que trabaja la persona en ese mes por el sueldo base
       
       TotalH= (Numb1H*30)*(Numb2H*100);
       
       txtRHoras.setText(String.valueOf(TotalH));
    }//GEN-LAST:event_btnCalcularHorasMouseClicked

