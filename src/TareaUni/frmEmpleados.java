    private void btnCalcularTemporalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcularTemporalMouseClicked
        int Numb1t, Numb2t, Totalt;
        
        Numb1t=Integer.parseInt(txtMesesCalculo.getText());
        Numb2t=Integer.parseInt(txtCantidadHijosCalculo.getText());
        
        //Al ser empleado temporal, su sueldo es fijo, lo unico que varia es por la cantidad de hijos que posea
        
        Totalt = (Numb1t*18000) +(1000*Numb2t);
        
        txtRTemporal.setText(String.valueOf(Totalt));
    }//GEN-LAST:event_btnCalcularTemporalMouseClicked

